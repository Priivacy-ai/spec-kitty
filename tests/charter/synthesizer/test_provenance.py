def test_dump_yaml_byte_reproducibility(tmp_path: Path, guard: PathGuard, sample_provenance: ProvenanceEntry) -> None:
    """Two dump_yaml calls with identical input produce byte-identical files."""
    path1 = tmp_path / "prov1.yaml"
    path2 = tmp_path / "prov2.yaml"
    dump_yaml(sample_provenance, path1, guard)
    dump_yaml(sample_provenance, path2, guard)
    assert path1.read_bytes() == path2.read_bytes()


def test_dump_yaml_hash_matches_canonical_yaml(tmp_path: Path, guard: PathGuard, sample_provenance: ProvenanceEntry) -> None:
    """artifact_content_hash computed via canonical_yaml matches dump_yaml bytes.

    This test validates that WP02's content hash (canonical_yaml → sha256) is
    byte-stable with WP03's on-disk serialization (dump_yaml uses canonical_yaml).
    """
    # Simulate the WP02 artifact body that would be written for this provenance.
    body = {
        "id": "HOW_003",
        "title": "How to apply directive 003",
        "summary": "A summary.",
    }
    artifact_bytes = canonical_yaml(body)
    expected_hash = hashlib.sha256(artifact_bytes).hexdigest()

    # Build a provenance entry with the matching content hash
    prov = ProvenanceEntry(
        schema_version="2",
        artifact_urn="tactic:how-we-apply-directive-003",
        artifact_kind="tactic",
        artifact_slug="how-we-apply-directive-003",
        artifact_content_hash=expected_hash,
        inputs_hash="b" * 64,
        adapter_id="fixture",
        adapter_version="1.0.0",
        synthesizer_version="3.2.0a5",
        source_section=None,
        source_urns=["directive:DIRECTIVE_003"],
        source_input_ids=["directive:DIRECTIVE_003"],
        generated_at="2026-04-17T12:00:00+00:00",
        produced_at="2026-01-01T00:00:00+00:00",
        corpus_snapshot_id="(none)",
        synthesis_run_id="01HTEST00000000000000TEST01",
    )

    out_path = tmp_path / "tactic-how-we-apply-directive-003.yaml"
    dump_yaml(prov, out_path, guard)
    loaded = load_yaml(out_path)

    # The content hash in the loaded provenance matches the canonical_yaml hash
    assert loaded.artifact_content_hash == expected_hash
