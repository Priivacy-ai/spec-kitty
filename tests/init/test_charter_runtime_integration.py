class TestCharterGracefulFallback:
    """Tests for graceful fallback when charter is missing."""

    def test_runs_without_charter(self, tmp_path: Path):
        """Test that spec-kitty commands run without charter file."""
        # Create minimal project structure (no charter)
        kittify_dir = tmp_path / ".kittify"
        kittify_dir.mkdir()

        memory_dir = kittify_dir / "memory"
        memory_dir.mkdir()

        # No charter.md file

        # Verify directory structure works without charter
        assert kittify_dir.exists()
        assert memory_dir.exists()
        assert not (memory_dir / "charter.md").exists()

        # Create a minimal config file
        config_file = kittify_dir / "config.yaml"
        config_file.write_text("version: '0.12.0'\n", encoding="utf-8")

        assert config_file.exists()

    def test_warns_when_charter_missing(self, tmp_path: Path):
        """Test that missing charter is handled gracefully (no errors)."""
        memory_dir = tmp_path / ".kittify" / "memory"
        memory_dir.mkdir(parents=True)

        # No charter file
        charter_path = memory_dir / "charter.md"

        # Check file doesn't exist
        assert not charter_path.exists()

        # This should not raise an error - commands should work without it
        # (In real code, commands check if file exists before reading)
        content = charter_path.read_text(encoding="utf-8") if charter_path.exists() else None

        assert content is None

    def test_uses_default_templates_without_charter(self, tmp_path: Path):
        """Test that default templates are used when charter is missing."""
        # Create project without charter
        kittify_dir = tmp_path / ".kittify"
