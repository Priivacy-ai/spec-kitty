    def test_charter_accessible_from_worktree(self, tmp_path: Path):
        """Test that charter is accessible from worktree context."""
        # Create main repo with charter
        repo = tmp_path / "repo"
        repo.mkdir()

        subprocess.run(["git", "init"], cwd=repo, check=True, capture_output=True)
        subprocess.run(
            ["git", "config", "user.name", "Test"],
            cwd=repo,
            check=True,
            capture_output=True,
        )
        subprocess.run(
            ["git", "config", "user.email", "test@example.com"],
            cwd=repo,
            check=True,
            capture_output=True,
        )

        # Create charter in main repo
        memory_dir = repo / ".kittify" / "memory"
        memory_dir.mkdir(parents=True)

        charter_file = memory_dir / "charter.md"
        charter_file.write_text("# Main Charter\n\nTest content", encoding="utf-8")

        # Create initial commit
        (repo / "README.md").write_text("test")
        subprocess.run(["git", "add", "."], cwd=repo, check=True, capture_output=True)
        subprocess.run(
            ["git", "commit", "-m", "init"],
            cwd=repo,
            check=True,
            capture_output=True,
        )

        # Create worktree
        worktree_dir = repo / ".worktrees" / "feature-lane-a"
        subprocess.run(
            ["git", "worktree", "add", str(worktree_dir), "-b", "kitty/mission-feature-lane-a"],
            cwd=repo,
            check=True,
            capture_output=True,
        )

        # Charter should be accessible from main repo
        # (Worktrees reference main repo's .kittify via relative path)
        assert charter_file.exists()

        # In real usage, commands running in worktree would reference
        # main repo's charter via path resolution
        content = charter_file.read_text(encoding="utf-8")
        assert "Main Charter" in content
